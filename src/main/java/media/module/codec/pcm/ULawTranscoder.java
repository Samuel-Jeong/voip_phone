    public static byte[] decode(byte[] media) {
        byte[] res = new byte[media.length * 2];
        int j = 0;

        for (byte b : media) {
            short s = muLawDecompressTable[b & 0xff];
            res[j++] = (byte) s;
            res[j++] = (byte) (s >> 8);
        }

        return res;
    }

